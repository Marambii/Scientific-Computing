# Integer
my_int = 42

# Float
my_float = 3.14

# Complex
my_complex = 1 + 2j

# List
my_list = [1, 2, 3, 4, 5]

# Tuple
my_tuple = (1, 2, 3, 4, 5)

# Dictionary
my_dict = {'name': 'Alice', 'age': 30, 'city': 'Wonderland'}

# Set
my_set = {1, 2, 3, 4, 5}

# Boolean
my_bool = True

# Print types
print(type(my_int))
print(type(my_float))
print(type(my_complex))
print(type(my_list))
print(type(my_tuple))
print(type(my_dict))
print(type(my_set))
print(type(my_bool))

# Convert integer to float and print
int_to_float = float(my_int)
print(int_to_float, type(int_to_float))

# Convert float to integer and print
float_to_int = int(my_float)
print(float_to_int, type(float_to_int))